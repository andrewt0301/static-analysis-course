package org.example.symbols;

/**
 * Symbol's access modifier.
 *
 * @since 2021/10/08
 */
public enum Access {
    /**
     * Local access.
     */
    LOCAL,

    /**
     * Private access.
     */
    PRIVATE,

    /**
     * Protected access.
     */
    PROTECTED,

    /**
     * Package access.
     */
    PACKAGE,

    /**
     * Public access.
     */
    PUBLIC
}
